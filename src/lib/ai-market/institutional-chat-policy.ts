export function getInstitutionalChatDisclosure(locale: MarketChatLocale) {
  return disclosures[locale];
}

export function ensureInstitutionalChatDisclosure(answer: string, locale: MarketChatLocale) {
  const disclosure = getInstitutionalChatDisclosure(locale);
  const withoutDuplicate = answer.split(disclosure).join("").trimEnd();

  return withoutDuplicate.trim() ? `${withoutDuplicate}\n\n${disclosure}` : disclosure;
}

