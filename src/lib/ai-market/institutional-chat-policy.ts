export function buildInstitutionalOpenAiRequest({
  model,
  question,
  contextText,
  history,
  locale,
  tier,
}: {
  model: string;
  question: string;
  contextText: string;
  history: Array<{ role: "user" | "assistant"; content: string }>;
  locale: MarketChatLocale;
  tier: InstitutionalChatTier;
}) {
  const historyText = history
    .map((message) => `${message.role === "assistant" ? "ASSISTANT_QUOTE" : "USER_QUOTE"}: ${message.content}`)
    .join("\n")
    .slice(-3_600);
  const body: Record<string, unknown> = {
    model,
    instructions: buildInstitutionalChatInstruction(locale, tier),
    input: [
      {
        role: "user",
        content: [
          {
            type: "input_text",
            text: [
              "<ENBILIR_SITE_CONTEXT>",
              contextText,
              "</ENBILIR_SITE_CONTEXT>",
              historyText ? `<RECENT_CHAT_QUOTED>\n${historyText}\n</RECENT_CHAT_QUOTED>` : "",
              `<USER_QUESTION>\n${question}\n</USER_QUESTION>`,
            ].filter(Boolean).join("\n\n"),
          },
        ],
      },
    ],
    max_output_tokens: tier === "VIP" ? 3_200 : 2_200,
    store: false,
  };

  if (tier === "VIP") {
    body.tools = [{ type: "web_search", search_context_size: "medium" }];
    body.tool_choice = "required";
    body.include = ["web_search_call.action.sources"];
  }

  return body;
}

function asOutputItems(value: unknown): OpenAiOutputItem[] {
  return Array.isArray(value) ? value.filter((item): item is OpenAiOutputItem => Boolean(item && typeof item === "object")) : [];
}

function asContentItems(value: unknown): OpenAiContent[] {
  return Array.isArray(value) ? value.filter((item): item is OpenAiContent => Boolean(item && typeof item === "object")) : [];
}

function asAnnotations(value: unknown): OpenAiAnnotation[] {
  return Array.isArray(value) ? value.filter((item): item is OpenAiAnnotation => Boolean(item && typeof item === "object")) : [];
}

function safeHttpsUrl(value: unknown) {
  if (typeof value !== "string") {
    return null;
  }

  try {
    const url = new URL(value);
    return url.protocol === "https:" && !url.username && !url.password ? url.toString() : null;
  } catch {
    return null;
  }
}

export function extractInstitutionalChatResult(payload: OpenAiResponsePayload): InstitutionalChatResult | null {
  const outputItems = asOutputItems(payload.output);
  const researched = outputItems.some((item) => item.type === "web_search_call");
  const contentBlocks = outputItems.flatMap((item) => asContentItems(item.content));
  const textBlocks = contentBlocks
    .filter((content) => typeof content.text === "string" && content.text.trim())
    .map((content) => ({ ...content, text: content.text as string }));
  const answer = textBlocks.length > 0
    ? textBlocks.map((content) => content.text).join("\n\n").trimEnd()
    : typeof payload.output_text === "string" ? payload.output_text.trim() : "";

  if (!answer) {
    return null;
  }

  const citations: InstitutionalChatCitation[] = [];
  let blockOffset = 0;

  for (const block of textBlocks) {
    const blockText = block.text as string;

    for (const annotation of asAnnotations(block.annotations)) {
      const url = annotation.type === "url_citation" ? safeHttpsUrl(annotation.url) : null;
      const startIndex = typeof annotation.start_index === "number" ? Math.round(annotation.start_index) : -1;
      const endIndex = typeof annotation.end_index === "number" ? Math.round(annotation.end_index) : -1;

      if (!url || startIndex < 0 || endIndex <= startIndex || endIndex > blockText.length) {
        continue;
      }

      citations.push({
        title: typeof annotation.title === "string" && annotation.title.trim()
          ? annotation.title.trim().slice(0, 240)
          : new URL(url).hostname,
        url,
        startIndex: blockOffset + startIndex,
        endIndex: blockOffset + endIndex,
      });
    }

    blockOffset += blockText.length + 2;
  }

  return {
    answer,
    citations: citations
      .sort((left, right) => left.startIndex - right.startIndex || left.endIndex - right.endIndex)
      .filter((citation, index, all) => index === 0 || citation.startIndex >= all[index - 1].endIndex)
      .slice(0, 16),
    researched,
  };
}
